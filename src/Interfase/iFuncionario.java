    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        boolean resultado = false;
        this.concesion = jComboBox4.getSelectedIndex()+1;
        this.turno = jComboTurno.getSelectedIndex()+1;
        if (jTextBusca5.getText().trim().isEmpty() || jTextBusca6.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(null,"El Nombre y/o Apellido no pueden quedar Vac√≠os");
        }else{
         if (concesion == 1){
                       switch (turno){
                           case 1: turno = 1;
                               break;
                           case 2: turno = 3;
                               break;
                           case 3: turno = 5;
                               break;
                        }
                }else{
                    turno = 2;
                }
          resultado  = opera.altaFuncionario(jTextBusca5.getText(), jTextBusca6.getText() , jComboBox3.getSelectedIndex()+1, 
                Integer.parseInt(jTextBusca.getText().trim()), jComboBox2.getSelectedIndex()+1, concesion, Integer.parseInt(jTextBusca3.getText()), turno, jRadioButton1.isSelected());
          if (resultado == true){
              JOptionPane.showMessageDialog(null,"Datos Ingresados Correctamente");
             this.hide();
          }else{
              JOptionPane.showMessageDialog(null,"Ocurrio algun error en el alta al funcionario");
          }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

